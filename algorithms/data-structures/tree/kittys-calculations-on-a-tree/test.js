const assert = require('assert');
const kitty  = require('.');

it('should return the kitty calculations on the given trees', () => {
    assert.deepEqual(
        kitty(7,
            [[1, 2], [1, 3], [1, 4], [3, 5], [3, 6], [3, 7]],
            [[2, 4], [5], [2, 4, 5], [2, 4, 7], [5, 6, 7], [2, 5, 6]]
        ),
        [16, 0, 106, 142, 214, 126]
    );

    assert.deepEqual(
        kitty(7,
            [[2, 1], [3, 1], [4, 1], [5, 3], [6, 3], [7, 3]],
            [[2, 4], [5], [2, 4, 5]]
        ),
        [16, 0, 106]
    );

    assert.deepEqual(
        kitty(8,
            [[2, 1], [3, 1], [4, 1], [5, 3], [6, 3], [7, 3], [8, 7]],
            [[2, 6, 7 ,8]]
        ),
        [426]
    );

    assert.deepEqual(
        kitty(81112,
            [[21111, 1], [31111, 1], [41111, 1], [51111, 31111], [61111, 31111], [71111, 31111], [81111, 71111]],
            [[21111, 61111, 71111, 81111]]
        ),
        [552888828]
    );
});
