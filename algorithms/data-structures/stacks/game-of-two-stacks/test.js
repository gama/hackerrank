const assert    = require('assert');
const twoStacks = require('.');

const testCases = [
    // limit  stackA            stackB          expected
    [  10,    [1],              [1],            2       ],
    [  10,    [1, 2],           [1],            3       ],
    [  10,    [1],              [1, 2],         3       ],
    [  10,    [1, 2],           [1, 2],         4       ],
    [  4,     [1, 2],           [1, 5],         3       ],
    [  4,     [1, 5],           [1, 2],         3       ],
    [  4,     [5, 1],           [1, 2],         2       ],
    [  4,     [1, 2],           [5, 1],         2       ],
    [  10,    [4, 2, 4, 6, 1],  [2, 1, 8, 5],   4       ],
    [  20,    [4, 2, 4, 6, 1],  [21],           5       ],
    [  20,    [21],             [2, 1, 8, 5],   4       ],
    [  20,    [2, 1, 8, 9],     [4, 5, 2, 6],   6       ],
    [  20,    [2, 1, 8, 9],     [4, 5, 2, 7],   5       ],
    [  10,    [8, 9],           [9, 2, 3, 8],   1       ],
    [  62,    [7, 15, 12, 0, 5, 18, 17, 2, 10, 15, 4, 2, 9, 15, 13, 12, 16], [12, 16, 6, 8, 16, 15, 18, 3, 11, 0, 17, 7, 6, 11, 14, 13, 15, 6, 18, 6, 16, 12, 16, 11, 16, 11],  6 ],
    [  19,    [14, 0, 15, 12, 15, 6, 15, 0, 18, 19, 16, 1, 3, 9, 5, 19, 0, 10, 10, 2, 14, 12, 1, 4, 6, 6, 10, 16, 7, 2, 14, 2], [2, 2, 6, 9, 0, 1, 1, 18, 12, 17, 11, 16, 18, 8, 7, 18, 19, 17, 13, 13, 2, 14, 10, 8, 0, 0, 4, 0, 2, 11, 2, 16], 5],
];

testCases.forEach(([limit, stackA, stackB, expected]) => {
    it(`should return the maximum possible score with inputs "${limit}, ${stackA}, ${stackB}"`, () => {
        assert.equal(twoStacks(limit, stackA, stackB), expected);
    });
});
